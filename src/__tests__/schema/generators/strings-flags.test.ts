// ─── Web generators ───────────────────────────────────────────────────────────

describe('generateWebTokens', () => {
  let dir: string
  afterEach(() => fs.rmSync(dir, { recursive: true, force: true }))

  it('generates CSS :root variables for .css output path', () => {
    const { config, dir: d } = makeConfig(EXAMPLE_STRINGS, EXAMPLE_FLAGS)
    dir = d
    generateWebTokens(config)
    // The test config uses tokens.css — should produce CSS custom properties only
    const output = fs.readFileSync(config.platforms.web!.output.tokens!, 'utf-8')

    expect(output).toContain('GENERATED BY SENTINEL')
    expect(output).toContain(':root {')
    expect(output).toContain('--color-')
    expect(output).not.toContain('export const tokens')
  })

  it('generates TypeScript exports for .ts output path', () => {
    const { config, dir: d } = makeConfig(EXAMPLE_STRINGS, EXAMPLE_FLAGS)
    dir = d
    // Override to use .ts extension
    const tsPath = config.platforms.web!.output.tokens!.replace('.css', '.ts')
    config.platforms.web!.output.tokens = tsPath
    generateWebTokens(config)
    const output = fs.readFileSync(tsPath, 'utf-8')

    expect(output).toContain('GENERATED BY SENTINEL')
    expect(output).toContain('export const tokens')
    expect(output).not.toContain(':root {')
  })
})

